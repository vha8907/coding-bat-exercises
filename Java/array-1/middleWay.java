/**
 * Given 2 int arrays, a and b, each length 3, return a new array length 2 containing their middle elements.
 */

public int[] middleWay(int[] a, int[] b) {
    int[] resultArray;                          // declare the array
    resultArray = new int[] {a[1],b[1]};        // multiple assignment of array elements for the output array
    return resultArray;
}
  